use UNIVER
select FACULTY.FACULTY, PULPIT.PULPIT, PROFESSION.PROFESSION_NAME, SUBJECT.SUBJECT_NAME, STUDENT.NAME,
case
	when (PROGRESS.NOTE = 6) then 'רוסע'
	when (PROGRESS.NOTE = 7) then 'סול'
	when (PROGRESS.NOTE = 8) then 'גמסול'
end [NOTE]
from STUDENT inner Join PROGRESS on STUDENT.IDSTUDENT = PROGRESS.IDSTUDENT 
inner join SUBJECT on PROGRESS.SUBJECT = SUBJECT.SUBJECT and (PROGRESS.NOTE between 6 and 8)
inner join GROUPS on STUDENT.IDGROUP = GROUPS.IDGROUP
inner join PULPIT on SUBJECT.PULPIT = PULPIT.PULPIT
inner join PROFESSION on PROFESSION.PROFESSION = GROUPS.PROFESSION
inner join FACULTY on GROUPS.FACULTY = FACULTY.FACULTY
order by FACULTY.FACULTY ASC, PULPIT.PULPIT ASC, PROFESSION.PROFESSION ASC, STUDENT.NAME ASC, PROGRESS.NOTE DESC;